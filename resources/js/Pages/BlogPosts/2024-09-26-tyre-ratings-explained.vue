            <p>
                The <span class="font-medium">fuel efficiency</span> rating measures how much energy (fuel) the tyre consumes as it rolls.
                Tyres that reduce rolling resistance are rated higher for fuel efficiency and can help you save on fuel costs.
                The rating scale ranges from <span class="font-medium">A (best)</span> to <span class="font-medium">E (poorest)</span>.
            </p>

            <ul class="list-disc pl-5 space-y-3">
                <li>
                    <span class="font-medium">A-rated</span>
