            <p>
                The <span class="font-medium">wet grip</span> rating measures how well a tyre stops on wet surfaces, which is crucial for safety in rainy conditions.
                Similar to fuel efficiency, the wet grip rating ranges from <span class="font-medium">A (best)</span> to <span class="font-medium">E (poorest)</span>.
            </p>

            <ul class="list-disc pl-5 space-y-3">
                <li>
                    <span class="font-medium">A-rated</span>
                    tyres provide the shortest braking distances on wet roads.
                </li>
                <li>
                    <span class="font-medium">E-rated</span>
