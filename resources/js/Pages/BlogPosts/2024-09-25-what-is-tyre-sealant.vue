            </p>

            <h3 class="text-2xl font-semibold">Limitations of Tyre Sealant</h3>

            <p>
                While tyre sealant can be a lifesaver in emergencies, it’s important to understand its limitations:
            </p>

            <h3 class="text-xl font-semibold">1. Temporary Solution</h3>

            <p>
                Tyre sealant is designed to be a <span class="font-medium">temporary fix</span>, not a permanent solution. While it can get you back on the road,
                you should always seek professional tyre repair as soon as possible after using sealant.
                The sealant can only fix small punctures, and larger holes or sidewall damage will require a more comprehensive repair.
            </p>

            <h3 class="text-xl font-semibold">2. May Affect Tyre Sensors</h3>

            <p>
