            <h3 class="text-2xl font-semibold">What is Tyre Sealant?</h3>

            <p>
                <span class="font-medium">Tyre sealant</span> is a liquid compound that’s designed to temporarily seal small punctures or leaks in your tyre.
                It is typically made of a sticky, latex-based substance that hardens when it comes into contact with air,
                creating a seal around the puncture. The sealant is usually injected into the tyre via the valve,
                allowing it to spread throughout the interior and fill the hole or leak.
            </p>

            <p>
                There are two main types of tyre sealants:
            </p>

            <ol class="list-decimal pl-5 space-y-3">
                <li>
                    <span class="font-medium">Pre-installed sealants:</span>
                    These are applied in the tyre during manufacturing or can be installed before a puncture happens.
                    They remain inside the tyre and act immediately when a puncture occurs.
                </li>
                <li>
                    <span class="font-medium">Emergency tyre sealants:</span>
                    These are designed to be used after a puncture has already happened. Drivers can carry a bottle of emergency tyre sealant in their vehicle and use it when needed.
                </li>
            </ol>

            <h3 class="text-2xl font-semibold">How Does Tyre Sealant Work?</h3>

            <p>
                The process is fairly straightforward. When you inject tyre sealant into a punctured tyre, the liquid spreads inside the tyre as it rotates.
                As it reaches the puncture or leak, the sealant reacts with the air, solidifying and plugging the hole.
                The tyre sealant essentially “patches” the inside of the tyre to prevent further air from escaping.
            </p>

            <p>
                Tyre sealants can seal small holes, typically up to <span class="font-medium">6mm</span> in diameter,
                but they are not a permanent fix. Once the sealant is applied,
                you should visit a professional to have the tyre properly repaired or replaced.
            </p>

