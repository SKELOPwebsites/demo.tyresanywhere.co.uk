                The sealant might clog the sensor, causing it to malfunction or provide inaccurate readings.
                If your car has TPMS, check if the tyre sealant you’re using is compatible with it.
            </p>

            <h3 class="text-xl font-semibold">3. Not Suitable for All Punctures</h3>

            <p>
                Tyre sealant works best for small punctures, typically less than 6mm in diameter.
                Larger punctures, gashes, or sidewall damage cannot be repaired with sealant.
                Additionally, tyre sealant isn’t effective for repairing slow leaks caused by worn-out valves or significant structural damage to the tyre.
            </p>

