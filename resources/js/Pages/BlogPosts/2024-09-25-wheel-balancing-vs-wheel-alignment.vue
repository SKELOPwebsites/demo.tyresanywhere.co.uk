            <h3 class="text-xl font-semibold">How Does Wheel Alignment Work?</h3>

            <p>
                When performing a wheel alignment, a technician adjusts the following:
            </p>

            <ul class="list-disc pl-5 space-y-3">
                <li>
                    <span class="font-medium">Camber:</span>
                    The angle of the tyre when viewed from the front of the vehicle. If the top of the tyre tilts in or out, the camber is out of alignment.
                </li>
                <li>
                    <span class="font-medium">Toe:</span>
                    The angle of the tyres when viewed from above. If the tyres point inward or outward, they are misaligned in the toe position.
                </li>
                <li>
                    <span class="font-medium">Caster:</span>
                    The angle of the steering axis when viewed from the side of the vehicle. Incorrect caster can affect the vehicle’s steering stability.
                </li>
            </ul>

            <h3 class="text-xl font-semibold">Signs You Need Wheel Alignment:</h3>

            <ul class="list-disc pl-5 space-y-3">
                <li>
                    The vehicle pulls to one side when driving.
                </li>
                <li>
                    Uneven or rapid tyre wear.
                </li>
                <li>
                    The steering wheel is off-center when driving straight.
                </li>
                <li>
                    The steering feels loose or unstable.
                </li>
            </ul>

