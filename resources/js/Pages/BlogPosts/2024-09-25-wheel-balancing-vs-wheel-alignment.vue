            <h3 class="text-2xl font-semibold">What is Wheel Balancing?</h3>

            <p>
                <span class="font-medium">Wheel balancing</span> involves ensuring that the weight of the tyre and wheel assembly is evenly distributed around the axle.
                Even the slightest imbalance in weight can cause vibrations at higher speeds, uneven tyre wear, and discomfort while driving.
                This process is essential for providing a smooth ride and prolonging tyre life.
            </p>

            <h3 class="text-xl font-semibold">How Does Wheel Balancing Work?</h3>

            <p>
                During a wheel balancing service, the technician will:
            </p>

            <ol class="list-decimal pl-5 space-y-3">
                <li>
                    Remove the wheels and place them on a machine that measures the imbalance.
                </li>
                <li>
                    Add small weights to the rim of the wheel to balance it out, ensuring even weight distribution across the wheel.
                </li>
                <li>
                    Reinstall the wheels and perform a test to ensure the vibration is eliminated.
                </li>
            </ol>

            <h3 class="text-xl font-semibold">Signs You Need Wheel Balancing:</h3>

            <ul class="list-disc pl-5 space-y-3">
                <li>
                    Vibrations in the steering wheel, seat, or floorboard when driving at higher speeds.
                </li>
                <li>
                    Uneven or premature tyre wear.
                </li>
                <li>
                    A noticeable drop in fuel efficiency.
                </li>
            </ul>

            <h3 class="text-2xl font-semibold">What is Wheel Alignment?</h3>

            <p>
                <span class="font-medium">Wheel alignment</span> (also known as tracking) involves adjusting the angles of the wheels so they are set to the manufacturer’s specifications.
                Proper alignment ensures that the tyres meet the road at the correct angle, providing even contact and better handling.
                Alignment affects the vehicle’s <span class="font-medium">camber</span>, <span class="font-medium">toe</span>, and <span class="font-medium">caster</span>
                — three key angles that control how the tyres touch the road.
            </p>

