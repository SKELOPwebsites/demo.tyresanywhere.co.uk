            <h3 class="text-2xl font-semibold">How Often Should You Get Wheel Balancing and Alignment?</h3>

            <ul class="list-disc pl-5 space-y-3">
                <li>
                    <span class="font-medium">Wheel Balancing:</span>
                    It’s recommended to get your wheels balanced every <span class="font-medium">5,000 to 6,000 miles</span> or when you notice any vibrations while driving.
                    Balancing is also required after getting new tyres, rotating your tyres, or repairing a flat tyre.
                </li>
                <li>
                    <span class="font-medium">Wheel Alignment:</span>
                    Wheel alignment should be checked at least once a year, but you should get it done sooner if you notice any signs of misalignment,
                    such as uneven tyre wear or your car pulling to one side. It’s also wise to get your alignment checked after hitting a pothole or curb,
                    as these can knock your wheels out of alignment.
                </li>
            </ul>

