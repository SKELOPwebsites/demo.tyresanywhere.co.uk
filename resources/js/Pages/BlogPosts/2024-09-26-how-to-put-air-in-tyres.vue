                    <span class="font-medium">Driver’s Side Door Jamb:</span>
                    Many cars have a sticker on the inside of the driver’s side door that lists the recommended pressure.
                </li>
                <li>
                    <span class="font-medium">Tyre Itself:</span>
                    Some tyres have the maximum PSI (pounds per square inch) listed on the sidewall, though this is not necessarily the recommended pressure for everyday driving.
                </li>
            </ul>

            <p>
                Make sure to check whether the recommendation is different for the <span class="font-semibold">front and rear tyres</span>.
