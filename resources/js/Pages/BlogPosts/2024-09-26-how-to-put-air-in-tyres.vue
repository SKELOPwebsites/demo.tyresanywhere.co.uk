            <p>To put air in your tyres, you'll need:</p>

            <ul class="list-disc pl-5 space-y-3">
                <li>
                    <span class="font-medium">A Tyre Pressure Gauge:</span>
                    This tool helps you measure the current air pressure in your tyres.
                </li>
                <li>
                    <span class="font-medium">Air Compressor:</span>
                    You can find air compressors at most petrol stations, or you can purchase a portable one for home use.
                </li>
                <li>
                    <span class="font-medium">Valve Caps:</span>
                    Make sure your tyre valve caps are present before starting, as they help maintain pressure after inflation.
                </li>
            </ul>

            <h3 class="text-xl font-semibold">Step 3: Check the Current Tyre Pressure</h3>

            <p>
                Before adding air, you need to check the current pressure to see how much air is required. Here’s how to do it:
            </p>

            <ol class="list-decimal pl-5 space-y-3">
                <li>
                    Remove the <span class="font-medium">valve cap</span> from the tyre's valve stem.
                </li>
                <li>
                    Place the <span class="font-medium">tyre pressure gauge</span> firmly on the valve stem to get a reading.
                </li>
                <li>
                    Compare the reading with the recommended pressure for your vehicle.
                </li>
            </ol>

            <p>
                If your tyre is under-inflated, proceed to the next step to add air.
            </p>

            <h3 class="text-xl font-semibold">Step 4: Add Air to Your Tyres</h3>

            <p>
                Now that you know the current pressure, it’s time to add air:
            </p>

            <ol class="list-decimal pl-5 space-y-3">
                <li>
                    Attach the air compressor nozzle to the <span class="font-medium">valve stem</span>. You’ll hear a slight hissing sound as air moves into the tyre.
                </li>
                <li>
                    Inflate the tyre in <span class="font-medium">short bursts</span> to avoid over-inflation. Many air pumps have a built-in gauge, but if yours doesn’t, stop and check with your own pressure gauge frequently.
                </li>
                <li>
                    <span class="font-medium">Recheck the pressure</span> after each burst to ensure you don’t exceed the recommended PSI.
                </li>
                <li>
                    Once the tyre reaches the recommended pressure, remove the air hose and quickly screw the <span class="font-medium">valve cap</span> back onto the tyre.
                </li>
            </ol>

            <h3 class="text-xl font-semibold">Step 5: Repeat for All Tyres</h3>

            <p>
                Repeat the process for all four tyres. Make sure to check each tyre’s pressure individually, as they may not all require the same amount of air.
                Don’t forget to also check your spare tyre—keeping it properly inflated is just as important in case of emergencies.
            </p>

            <h3 class="text-xl font-semibold">Step 6: Double-Check for Leaks</h3>

            <p>
                After inflating all tyres, it's a good idea to check for any leaks. You can do this by:
            </p>

            <ol class="list-decimal pl-5 space-y-3">
                <li>
                    Listening for a <span class="font-medium">hissing sound</span> around the valve or tyre surface.
                </li>
                <li>
                    Applying a small amount of soapy water to the valve stem and tyre surface. If bubbles form, there may be a leak, and you should have the tyre inspected or repaired.
                </li>
            </ol>

            <h3 class="text-xl font-semibold">Step 7: Check Tyre Pressure Regularly</h3>

            <p>
                Tyre pressure fluctuates with temperature and general use, so it’s important to check your tyres <span class="font-medium">monthly</span> or before long trips.
                Keeping your tyres inflated to the recommended pressure ensures they wear evenly and last longer, saving you money on replacements.
            </p>

