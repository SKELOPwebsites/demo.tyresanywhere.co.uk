                Keeping your tyres properly inflated is essential for vehicle safety, fuel efficiency, and overall performance.
                Under-inflated tyres can lead to poor handling, decreased mileage, and even tyre blowouts.
                Follow this simple guide to make sure your tyres are always at the correct pressure
            </p>

            <h3 class="text-xl font-semibold">Step 1: Check the Recommended Tyre Pressure</h3>

            <p>
                Before you start inflating your tyres, you need to know the correct pressure. You can find the recommended tyre pressure in a few places:
            </p>

            <ul class="list-disc pl-5 space-y-3">
                <li>
                    <span class="font-medium">Your Vehicle’s Manual:</span>
                    The manual will indicate the optimal tyre pressure for your specific vehicle model.
