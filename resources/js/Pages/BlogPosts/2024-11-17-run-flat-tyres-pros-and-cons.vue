            <ol class="list-decimal pl-5 space-y-3">
                <li>
                    <span class="font-medium">Higher Cost</span>
                    <ul class="list-disc pl-5 space-y-3">
                        <li>
                            Run-flat tyres are more expensive than traditional tyres due to their advanced design and materials.
                        </li>
                        <li>
                            Replacement costs can also be higher.
                        </li>
                    </ul>
                </li>
                <li>
                    <span class="font-medium">Reduced Ride Comfort</span>
                    <ul class="list-disc pl-5 space-y-3">
                        <li>
                            The reinforced sidewalls make run-flat tyres stiffer, leading to a harsher ride compared to conventional tyres.
                        </li>
                    </ul>
                </li>
                <li>
                    <span class="font-medium">Limited Repairability</span>
                    <ul class="list-disc pl-5 space-y-3">
                        <li>
                            In most cases, run-flat tyres cannot be repaired after a puncture and need to be replaced entirely.
                        </li>
                    </ul>
                </li>
                <li>
                    <span class="font-medium">Shorter Lifespan</span>
                    <ul class="list-disc pl-5 space-y-3">
                        <li>
                            Run-flat tyres tend to wear out faster than traditional tyres, increasing maintenance costs over time.
                        </li>
                    </ul>
                </li>
                <li>
                    <span class="font-medium">Distance Limitations</span>
                    <ul class="list-disc pl-5 space-y-3">
                        <li>
                            While they allow continued driving after a puncture, the limited range (typically 50 miles) may not be sufficient in remote areas without nearby service centers.
                        </li>
                    </ul>
                </li>
                <li>
                    <span class="font-medium">Availability Issues</span>
                    <ul class="list-disc pl-5 space-y-3">
                        <li>
                            Not all garages or tyre retailers stock run-flat tyres, potentially causing delays when replacements are needed.
                        </li>
                    </ul>
                </li>
            </ol>

