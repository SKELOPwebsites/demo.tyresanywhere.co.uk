            <h3 class="text-xl font-semibold">What Are Run-Flat Tyres?</h3>

            <p>
                Run-flat tyres are designed to support the weight of a vehicle temporarily, even after a loss of air pressure due to a puncture or damage. They are reinforced with tougher sidewalls or internal support systems that prevent the tyre from collapsing when deflated. With run-flat tyres, drivers can continue traveling for a limited distance, typically up to 50 miles, at reduced speeds of up to 50 mph.
            </p>

            <p>
                There are two main types of run-flat tyres:
            </p>

            <ol class="list-decimal pl-5 space-y-3">
                <li>
                    <span class="font-medium">Self-Supporting Tyres:</span>
                    Equipped with reinforced sidewalls to carry the vehicle's load during a puncture.
                </li>
                <li>
                    <span class="font-medium">Support Ring Tyres: </span>
                    Feature an internal ring structure that supports the tyre even when it loses pressure.
                </li>
            </ol>

            <h3 class="text-xl font-semibold">Pros of Run-Flat Tyres</h3>

            <ol class="list-decimal pl-5 space-y-3">
