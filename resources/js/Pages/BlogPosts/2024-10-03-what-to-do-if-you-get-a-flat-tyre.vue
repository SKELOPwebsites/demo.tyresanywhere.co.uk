            <h3 class="text-xl font-semibold">1. Stay Calm and Find a Safe Spot to Stop</h3>

            <p>
                The first thing to remember if you get a flat tyre is to remain calm. Panicking can lead to rash decisions that put you in more danger. If you feel your tyre go flat, follow these steps:
            </p>

            <ul class="list-disc pl-5 space-y-3">
                <li>
                    <span class="font-medium">Slow down gradually:</span>
                    Don’t slam on the brakes; instead, ease off the accelerator and slowly reduce your speed.
                </li>
                <li>
                    <span class="font-medium">Look for a safe place to stop:</span>
                    Ideally, you want to pull over to a flat, stable area away from traffic. A parking lot, rest area, or the shoulder of a straight road are good options.
                </li>
                <li>
                    <span class="font-medium">Avoid stopping on a curve or incline:</span>
                    These areas can be hazardous as other drivers may not see you in time to avoid a collision.
                </li>
                <li>
                    <span class="font-medium">Turn on your hazard lights:</span>
                    This alerts other drivers to your situation, helping them see you from a distance and avoid your vehicle.
                </li>
            </ul>

            <h3 class="text-xl font-semibold">2. Ensure Your Safety Before Exiting the Vehicle</h3>

            <p>
                Before you get out of the car, it’s important to prioritize safety:
            </p>

            <ul class="list-disc pl-5 space-y-3">
                <li>
                    <span class="font-medium">Turn on your hazard lights</span>
                    to warn other drivers that you’re stopped.
                </li>
                <li>
                    <span class="font-medium">Engage the parking brake</span>
                    to prevent your vehicle from rolling.
                </li>
                <li>
                    If you have one, place a
                    <span class="font-medium">reflective triangle or flares</span>
                    a few feet behind your vehicle to increase visibility, especially in low-light conditions or on busy roads.
                </li>
            </ul>

            <p>
                Make sure you check your surroundings carefully before exiting the vehicle, especially if you're on a busy road or highway.
            </p>

            <h3 class="text-xl font-semibold">3. Assess the Damage</h3>

            <p>
                Once you’re safely stopped and out of the car, assess the situation. Walk around your vehicle and inspect the tyre to determine the extent of the damage:
            </p>
            <ul class="list-disc pl-5 space-y-3">
                <li>
                    <span class="font-medium">Is the tyre completely flat or just low on air?</span>
                </li>
                <li>
                    <span class="font-medium">Is there visible damage like a puncture, cut, or tear?</span>
                </li>
                <li>
                    <span class="font-medium">Is the rim or wheel damaged?</span>
                </li>
            </ul>
            <p>
                If the tyre is completely flat or the damage is significant, it will likely need to be changed or repaired before you can continue driving.
            </p>

