                </li>
                <li>
                    <span class="font-medium">Lower the vehicle completely:</span>
                    Remove the jack and fully lower the vehicle. Check the lug nuts again to ensure they’re as tight as possible.
                </li>
                <li>
                    <span class="font-medium">Store the flat tyre:</span>
                    Place the flat tyre, jack, and lug wrench back in your vehicle.
