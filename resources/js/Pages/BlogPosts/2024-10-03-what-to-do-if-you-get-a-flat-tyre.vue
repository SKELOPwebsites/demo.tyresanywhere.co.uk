                <li>
                    <span class="font-medium">Loosen the lug nuts:</span>
                    Before lifting the vehicle, use the lug wrench to slightly loosen the lug nuts on the flat tyre. Turn them counterclockwise, but don’t remove them completely yet.
                </li>
                <li>
                    <span class="font-medium">Position the jack:</span>
                    Place the jack under the vehicle at the recommended lifting point (usually near the flat tyre). Refer to your owner’s manual for the correct location. Make sure the jack is stable on the ground before lifting.
                </li>
                <li>
                    <span class="font-medium">Lift the vehicle:</span>
                    Use the jack to lift the vehicle until the flat tyre is about 6 inches off the ground. Ensure the vehicle is stable before proceeding.
                </li>
                <li>
                    <span class="font-medium">Remove the lug nuts:</span>
                    Fully unscrew the loosened lug nuts and set them aside. Keep them in a safe spot where they won’t roll away.
                </li>
                <li>
                    <span class="font-medium">Carefully pull the flat tyre off the wheel hub and set it aside.:</span>
