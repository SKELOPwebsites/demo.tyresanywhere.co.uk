                    <span class="font-medium">Install the spare tyre:</span>
                    Align the spare tyre with the wheel bolts and slide it onto the hub. Push it in place until the wheel is secure.
                </li>
                <li>
                    <span class="font-medium">Hand-tighten the lug nuts:</span>
                    Start threading the lug nuts onto the bolts by hand. Tighten them as much as you can by hand before lowering the vehicle.
                </li>
                <li>
                    <span class="font-medium">Lower the vehicle:</span>
                    Use the jack to carefully lower the vehicle until the spare tyre is in full contact with the ground, but don’t remove the jack yet.
