                </li>
                <li>
                    <span class="font-medium">Use a tyre repair kit:</span>
                    Some vehicles come equipped with a tyre repair kit that allows you to seal minor punctures and reinflate the tyre temporarily.
                </li>
                <li>
                    <span class="font-medium">Use a mobile tyre service:</span>
                    In some areas, you can call a mobile tyre-fitting service to replace your tyre on-site.
                </li>
