                    Most roadside assistance services can quickly change a flat tyre or tow your vehicle if necessary.
                </li>
                <li>
                    <span class="font-medium">Using a tyre repair kit:</span>
                    If the damage is minor (like a small puncture), and you have a tyre repair kit, you may be able to patch the tyre temporarily until you can get to a mechanic.
                </li>
            </ul>

