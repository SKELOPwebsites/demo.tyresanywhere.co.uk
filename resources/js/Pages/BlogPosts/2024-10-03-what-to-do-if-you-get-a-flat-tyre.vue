            <h3 class="text-xl font-semibold">7. Preventing Future Flat Tyres</h3>

            <p>
                Flat tyres can happen unexpectedly, but you can reduce the risk by taking some preventative measures:
            </p>
            <ul class="list-disc pl-5 space-y-3">
                <li>
                    <span class="font-medium">Check tyre pressure regularly:</span>
                    Under-inflated tyres are more prone to blowouts and punctures. Make sure your tyres are properly inflated to the recommended levels.
                </li>
                <li>
                    <span class="font-medium">Inspect your tyres for damage:</span>
                    Regularly check your tyres for cuts, bulges, or foreign objects that could lead to a flat.
                </li>
                <li>
                    <span class="font-medium">Rotate your tyres:</span>
                    Rotating your tyres regularly ensures even wear, which reduces the likelihood of a flat.
                </li>
                <li>
                    <span class="font-medium">Avoid driving over debris or potholes:</span>
                    Being cautious of the road conditions can help prevent tyre damage.
                </li>
            </ul>


