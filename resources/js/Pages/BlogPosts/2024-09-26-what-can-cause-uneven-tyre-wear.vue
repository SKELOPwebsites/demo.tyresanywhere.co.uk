                In this post, we’ll explore the main reasons why tyres wear unevenly and how to avoid these issues to keep your tyres in top condition.
            </p>

            <h3 class="text-2xl font-semibold">1. Incorrect Tyre Pressure</h3>

            <p>
                One of the most common causes of uneven tyre wear is <span class="font-medium">incorrect tyre pressure</span>.
                Under-inflated or over-inflated tyres can cause specific areas of the tyre to wear faster than others.
            </p>

            <ul class="list-disc pl-5 space-y-3">
                <li>
                    <span class="font-medium">Under-inflated tyres:</span>
                    When tyres don’t have enough air pressure, the outer edges of the tread will wear down faster.
                    This happens because more of the tyre's surface is in contact with the road, leading to excessive wear on the edges.
                </li>
                <li>
                    <span class="font-medium">Over-inflated tyres:</span>
                    Tyres with too much air pressure will cause the center of the tyre tread to wear out more quickly, as the tyre bulges and reduces the contact patch.
                </li>
            </ul>

            <h3 class="text-xl font-semibold">How to Prevent It:</h3>

            <p>
                Check your tyre pressure regularly, ideally once a month, and before long trips.
                Make sure your tyres are inflated to the manufacturer’s recommended pressure,
                which can be found in your vehicle’s manual or on the driver’s side door frame.
            </p>

